-- ============================================================
-- TABLES
-- ============================================================

CREATE TABLE customer (
    customerNumber  INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY
                    (START WITH 1000001 INCREMENT BY 1 CACHE 20),
    firstName       CHAR(10),
    lastName        CHAR(20),
    dateOfBirth     DATE,
    houseName       CHAR(20),
    houseNumber     CHAR(4),
    postcode        CHAR(8),
    phonehome       CHAR(20),
    phonemobile     CHAR(20),
    emailaddress    CHAR(100),
    PRIMARY KEY (customerNumber)
);

CREATE TABLE customer_secure (
    customerNumber  INTEGER NOT NULL,
    customerPass    CHAR(32),
    state_indicator CHAR(1),
    pass_changes    INTEGER,
    PRIMARY KEY (customerNumber),
    FOREIGN KEY (customerNumber)
        REFERENCES customer (customerNumber)
        ON DELETE CASCADE
);

CREATE TABLE policy (
    policyNumber     INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY
                     (START WITH 1000001 INCREMENT BY 1 CACHE 20),
    customerNumber   INTEGER NOT NULL,
    issueDate        DATE,
    expiryDate       DATE,
    policyType       CHAR(1),
    lastChanged      TIMESTAMP NOT NULL DEFAULT NOW(),
    brokerId         INTEGER,
    brokersReference CHAR(10),
    payment          INTEGER,
    commission       SMALLINT,
    PRIMARY KEY (policyNumber),
    FOREIGN KEY (customerNumber)
        REFERENCES customer (customerNumber)
        ON DELETE CASCADE
);

CREATE TABLE endowment (
    policyNumber  INTEGER NOT NULL,
    equities      CHAR(1),
    withProfits   CHAR(1),
    managedFund   CHAR(1),
    fundName      CHAR(10),
    term          SMALLINT,
    sumAssured    INTEGER,
    lifeAssured   CHAR(31),
    paddingData   VARCHAR(32606),
    PRIMARY KEY (policyNumber),
    FOREIGN KEY (policyNumber)
        REFERENCES policy (policyNumber)
        ON DELETE CASCADE
);

CREATE TABLE house (
    policyNumber  INTEGER NOT NULL,
    propertyType  CHAR(15),
    bedrooms      SMALLINT,
    value         INTEGER,
    houseName     CHAR(20),
    houseNumber   CHAR(4),
    postcode      CHAR(8),
    PRIMARY KEY (policyNumber),
    FOREIGN KEY (policyNumber)
        REFERENCES policy (policyNumber)
        ON DELETE CASCADE
);

CREATE TABLE motor (
    policyNumber      INTEGER NOT NULL,
    make              CHAR(15),
    model             CHAR(15),
    value             INTEGER,
    regNumber         CHAR(7),
    colour            CHAR(8),
    cc                SMALLINT,
    yearOfManufacture DATE,
    premium           INTEGER,
    accidents         INTEGER,
    PRIMARY KEY (policyNumber),
    FOREIGN KEY (policyNumber)
        REFERENCES policy (policyNumber)
        ON DELETE CASCADE
);

CREATE TABLE commercial (
    policyNumber    INTEGER      NOT NULL,
    requestDate     TIMESTAMP,
    startDate       DATE,
    renewalDate     DATE,
    address         CHAR(255),
    zipcode         CHAR(8),
    latitudeN       CHAR(11),
    longitudeW      CHAR(11),
    customer        CHAR(255),
    propertyType    CHAR(255),
    firePeril       SMALLINT,
    firePremium     INTEGER,
    crimePeril      SMALLINT,
    crimePremium    INTEGER,
    floodPeril      SMALLINT,
    floodPremium    INTEGER,
    weatherPeril    SMALLINT,
    weatherPremium  INTEGER,
    status          SMALLINT,
    rejectionReason CHAR(255),
    PRIMARY KEY (policyNumber),
    FOREIGN KEY (policyNumber)
        REFERENCES policy (policyNumber)
        ON DELETE CASCADE
);

CREATE TABLE claim (
    claimNumber   INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY
                  (START WITH 1000001 INCREMENT BY 1 CACHE 20),
    policyNumber  INTEGER NOT NULL,
    claimDate     DATE,
    paid          INTEGER,
    value         INTEGER,
    cause         CHAR(255),
    observations  CHAR(255),
    PRIMARY KEY (claimNumber),
    FOREIGN KEY (policyNumber)
        REFERENCES policy (policyNumber)
        ON DELETE CASCADE
);

